
// конвертнуть {[], [], []} в [{}, {}, {}]

const obj = {
  0: [12345, `12345`],
  1: [12346, `12346`],
  2: [12347, `12347`],
  3: [12348, `12348`],
  4: [12349, `12349`],
  5: [12350, `12350`]
};

const arr = Object
  .values(obj)
  .reduce((acc, cur) => {
    acc.push({ [cur[0]]: cur[1] })
    return acc
  }, []);

console.log('arr: ', arr);
